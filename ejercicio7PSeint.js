let largo = parseFloat(prompt("Ingrese el largo de un Rectangulo: "));
let ancho = parseFloat(prompt("Ingrese el ancho de un Rectangulo: "));
let perímetro = 2 * (largo + ancho);
document.writeln("El perimetro del rectangulo es: ", perímetro);
let área = largo * ancho;
document.writeln("El área del rectangulo es: ", área);
